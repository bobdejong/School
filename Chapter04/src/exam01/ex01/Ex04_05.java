package exam01.ex01;

public class Ex04_05 {

	public static void main(String[] args) {
		int a=100, b=200;
		//관계 연산자
		System.out.printf("%d==%d는 %s 이다.\n",a,b,a==b);  //같냐?
		System.out.printf("%d!=%d는 %s 이다.\n",a,b,a!=b); //같지 않냐?
		System.out.printf("%d>%d는 %s 이다.\n",a,b,a>b);
		System.out.printf("%d<%d는 %s 이다.\n",a,b,a<b);
		System.out.printf("%d>=%d는 %s 이다.\n",a,b,a>=b);
		System.out.printf("%d<=%d는 %s 이다.\n",a,b,a<=b);
		System.out.printf("%d=%d는 %s 이다.\n",a,b,a=b);  //대입연산자
		
	}

}
